package com.youxianji.facade.yxjia.bean.json;

public class JIAShopBean {

	private String shopid;//	String	店铺ID
	private String shopname;//	String	店铺名称
	private String shopaddress;//	String	店铺地址
	private String shoptelephone;//	String	店铺联系方式
	private String imageurl;//	String	店铺图片地址
	public String getShopid() {
		return shopid;
	}
	public void setShopid(String shopid) {
		this.shopid = shopid;
	}
	public String getShopname() {
		return shopname;
	}
	public void setShopname(String shopname) {
		this.shopname = shopname;
	}
	public String getShopaddress() {
		return shopaddress;
	}
	public void setShopaddress(String shopaddress) {
		this.shopaddress = shopaddress;
	}
	public String getShoptelephone() {
		return shoptelephone;
	}
	public void setShoptelephone(String shoptelephone) {
		this.shoptelephone = shoptelephone;
	}
	public String getImageurl() {
		return imageurl;
	}
	public void setImageurl(String imageurl) {
		this.imageurl = imageurl;
	}
	
	
	
}
